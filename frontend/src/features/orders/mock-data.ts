export function getDemoOrderDetail(orderId: string) {
  // Try finding by exact ID first
  const found = DEMO_ORDER_DETAILS.find((order) => order.id === orderId);
  if (found) return found;

  // Fallback to finding by numeric order-XX mapping
  const numericId = orderId.replace("client-order-", "").replace("order-", "");
  return DEMO_ORDER_DETAILS.find((order) => order.number === numericId);
}
